package com.people2000.user.business.utils.security;

/**
 * 抽象的非称加密类
 * 
 * @version 1.0
 * @author wangpengfei
 *
 */
public class AbstractAsymPasswordEncrypt {
	protected String PrimeKey;
	protected String PublicKey;

	public String getPrimeKey() {
		return PrimeKey;
	}

	public void setPrimeKey(String primeKey) {
		PrimeKey = primeKey;
	}

	public String getPublicKey() {
		return PublicKey;
	}

	public void setPublicKey(String publicKey) {
		PublicKey = publicKey;
	}

}
