package com.people2000.user.model.dto;

import java.io.Serializable;
import java.math.BigDecimal;

public class AccountRechargeDTO implements Serializable {

	/**
	 */
	private static final long serialVersionUID = 7897832321779568023L;
	private Long accountId;// 账户id
	private Long userId;// 用户id
	private BigDecimal amount;// 充值金额
	private String optCommont;// 操作备注

	public Long getAccountId() {
		return accountId;
	}

	public void setAccountId(Long accountId) {
		this.accountId = accountId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getOptCommont() {
		return optCommont;
	}

	public void setOptCommont(String optCommont) {
		this.optCommont = optCommont;
	}

}
