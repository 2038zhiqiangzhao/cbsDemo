public class FunctionTreeDTO implements Serializable {
	private Long id;
	private String code; // 编码
	private String parentCode; // 父编码
	private Integer productType; // 产品类型
	private Integer type; // 功能类型(1.菜单, 2.按钮)
	private String domain; // context
	private String path; // url
	private String name; // 功能名称
	private String desc; // 功能描述
	private Integer level; // 菜单层级
	private String target; // 跳转类型当为null转转换为0当前页面跳转.1为新页面跳转.
	private String icon; // 挨坑 :) 使用Glyphicon 字体图标实例 举例 : 对号 glyphicon-ok 存储为
							// \\e013??

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getParentCode() {
		return parentCode;
	}

	public void setParentCode(String parentCode) {
		this.parentCode = parentCode;
	}

	public Integer getProductType() {
		return productType;
	}

	public void setProductType(Integer productType) {
		this.productType = productType;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public Integer getLevel() {
		return level;
	}

	public void setLevel(Integer level) {
		this.level = level;
	}
}
