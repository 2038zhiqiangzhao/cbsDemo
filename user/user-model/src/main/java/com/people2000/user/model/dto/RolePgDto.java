public class RolePgDto extends Role implements Serializable {

	private static final long serialVersionUID = 1L;

	private Integer currentPage;// 当前页

	private Integer itemsPerPage;// 每页显示条数

	private Boolean pgFlg;// 是否分页

	private String begin;// 创建时间开始

	private String end;// 创建时间结束

	public Integer getCurrentPage() {
		return currentPage;
	}

	public void setCurrentPage(Integer currentPage) {
		this.currentPage = currentPage;
	}

	public Integer getItemsPerPage() {
		return itemsPerPage;
	}

	public void setItemsPerPage(Integer itemsPerPage) {
		this.itemsPerPage = itemsPerPage;
	}

	public Boolean isPgFlg() {
		return pgFlg;
	}

	public void setPgFlg(Boolean pgFlg) {
		this.pgFlg = pgFlg;
	}

	public String getBegin() {
		return begin;
	}

	public void setBegin(String begin) {
		this.begin = begin;
	}

	public String getEnd() {
		return end;
	}

	public void setEnd(String end) {
		this.end = end;
	}

	public Boolean getPgFlg() {
		return pgFlg;
	}

}
