package com.people2000.user.model.dto;

import java.io.Serializable;
import java.util.List;

/**
 * Created by xiaole on 2016/2/25.
 */
public class RoleUpdateDTO implements Serializable {
	// 用户id
	private Long userId;
	// 删除的角色列表
	private List<Long> delRoles;
	// 新增的角色列表
	private List<Long> addRoles;

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public List<Long> getDelRoles() {
		return delRoles;
	}

	public void setDelRoles(List<Long> delRoles) {
		this.delRoles = delRoles;
	}

	public List<Long> getAddRoles() {
		return addRoles;
	}

	public void setAddRoles(List<Long> addRoles) {
		this.addRoles = addRoles;
	}
}
