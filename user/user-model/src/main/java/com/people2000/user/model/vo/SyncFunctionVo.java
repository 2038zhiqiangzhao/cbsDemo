package com.people2000.user.model.vo;

import java.io.Serializable;

/**
 * @author crc
 * @time 2015-8-11 下午4:41:27
 * @description <pre>
 * 子账户
 * </pre>
 */
public class SyncFunctionVo implements Serializable {

	private String syncUrl;// 同步url

	private String md5Code;// 远端校验MD5

	private String codeHead;// 同步的code码开头

	private String type;// 1：生成MD5码，并发送邮件2：同步functions

	private String updateTime;// 根据updatetime去筛选

	private String functionList;// 同步functionListjson格式的字符串

	private String validationKey;// 验证密匙

	public String getSyncUrl() {
		return syncUrl;
	}

	public void setSyncUrl(String syncUrl) {
		this.syncUrl = syncUrl;
	}

	public String getMd5Code() {
		return md5Code;
	}

	public void setMd5Code(String md5Code) {
		this.md5Code = md5Code;
	}

	public String getCodeHead() {
		return codeHead;
	}

	public void setCodeHead(String codeHead) {
		this.codeHead = codeHead;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getFunctionList() {
		return functionList;
	}

	public void setFunctionList(String functionList) {
		this.functionList = functionList;
	}

	public String getValidationKey() {
		return validationKey;
	}

	public void setValidationKey(String validationKey) {
		this.validationKey = validationKey;
	}

}
