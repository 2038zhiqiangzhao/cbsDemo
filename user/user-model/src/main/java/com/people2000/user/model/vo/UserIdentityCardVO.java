package com.people2000.user.model.vo;

import java.io.Serializable;

/**
 * Created by xiaole on 2015/10/9.
 */
public class UserIdentityCardVO implements Serializable {
	private Long id;
	private Long userId;
	private String identityCardName;
	private String identityCardNumber;
	private String identityCardFrontUrl;
	private String identityCardBackUrl;
	private Integer status;
	private Integer type;// 证件类型
	private Integer notpassCode;// 未通过错误码
	private String notpassMessage;// 未通过错误信息
	private String identityCardHandUrl;// 手持照url

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getNotpassCode() {
		return notpassCode;
	}

	public void setNotpassCode(Integer notpassCode) {
		this.notpassCode = notpassCode;
	}

	public String getNotpassMessage() {
		return notpassMessage;
	}

	public void setNotpassMessage(String notpassMessage) {
		this.notpassMessage = notpassMessage;
	}

	public String getIdentityCardHandUrl() {
		return identityCardHandUrl;
	}

	public void setIdentityCardHandUrl(String identityCardHandUrl) {
		this.identityCardHandUrl = identityCardHandUrl;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getIdentityCardName() {
		return identityCardName;
	}

	public void setIdentityCardName(String identityCardName) {
		this.identityCardName = identityCardName;
	}

	public String getIdentityCardNumber() {
		return identityCardNumber;
	}

	public void setIdentityCardNumber(String identityCardNumber) {
		this.identityCardNumber = identityCardNumber;
	}

	public String getIdentityCardFrontUrl() {
		return identityCardFrontUrl;
	}

	public void setIdentityCardFrontUrl(String identityCardFrontUrl) {
		this.identityCardFrontUrl = identityCardFrontUrl;
	}

	public String getIdentityCardBackUrl() {
		return identityCardBackUrl;
	}

	public void setIdentityCardBackUrl(String identityCardBackUrl) {
		this.identityCardBackUrl = identityCardBackUrl;
	}
}
