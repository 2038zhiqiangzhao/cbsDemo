package com.people2000.user.model.vo;

import java.io.Serializable;
import java.util.Date;
import java.util.Map;

/**
 * 从老系统迁移的用户信息包装类
 * 
 * @author ody
 *
 */
public class UserInfoFromOldVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7589217054095112607L;

	private String username;// 用户名

	private String nickname;// 昵称

	private String identityCardName;// 真实姓名

	private String mobile;// 真实姓名

	private String headPicUrl;// 头像地址

	private String password;// 密码(未加密)

	private String email;// 邮箱

	private String address;// 地址

	private String province;// 省

	private String city;// 城市

	private Integer sex;// 性别

	private Date birthday;// 生日

	private String qq;

	private String oldSystemId;// 老系统id

	private String jsonStringInfo;// 从老系统登陆后获得的返回信息

	private Map<UserExtEnum, String> map;// 老系统存在的,新系统没有的字段的信息

	private Integer status;

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public Integer getSex() {
		return sex;
	}

	public void setSex(Integer sex) {
		this.sex = sex;
	}

	public Date getBirthday() {
		return birthday;
	}

	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}

	public String getQq() {
		return qq;
	}

	public void setQq(String qq) {
		this.qq = qq;
	}

	public Map<UserExtEnum, String> getMap() {
		return map;
	}

	public void setMap(Map<UserExtEnum, String> map) {
		this.map = map;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getHeadPicUrl() {
		return headPicUrl;
	}

	public void setHeadPicUrl(String headPicUrl) {
		this.headPicUrl = headPicUrl;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getJsonStringInfo() {
		return jsonStringInfo;
	}

	public void setJsonStringInfo(String jsonStringInfo) {
		this.jsonStringInfo = jsonStringInfo;
	}

	public String getOldSystemId() {
		return oldSystemId;
	}

	public void setOldSystemId(String oldSystemId) {
		this.oldSystemId = oldSystemId;
	}

	public String getIdentityCardName() {
		return identityCardName;
	}

	public void setIdentityCardName(String identityCardName) {
		this.identityCardName = identityCardName;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

}
